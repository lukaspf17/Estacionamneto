    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().isEmpty() || jFormattedTextField4.getText().isEmpty() || jFormattedTextField2.getText().isEmpty() || jFormattedTextField3.getText().isEmpty() || idCliente == 0){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para atualizar!");
        } else {
            ClienteVeiculoController controller = new ClienteVeiculoController();
            ClienteVeiculoDTO clienteVeiculoDto = new ClienteVeiculoDTO(idCliente, jTextField1.getText(), jFormattedTextField4.getText(), jFormattedTextField2.getText(), jFormattedTextField3.getText());
            controller.updateClienteVeiculo(clienteVeiculoDto);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

