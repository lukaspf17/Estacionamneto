    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jFormattedTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos o campos para buscar!");
        } else {
            ClienteVeiculoController clienteVeiculo = new ClienteVeiculoController();
            ClienteVeiculoDTO clienteDto = clienteVeiculo.bucarCliente(jFormattedTextField1.getText());
                       
            if(clienteDto.getCpfCliente() == null){
                JOptionPane.showMessageDialog(null, "CPF inserido não consta nos registros.");
            } else{
                jTextField1.setText(clienteDto.getNomeCliente());
                jFormattedTextField4.setText(clienteDto.getCpfCliente());
                jFormattedTextField2.setText(clienteDto.getTelefoneCliente());
                jFormattedTextField3.setText(clienteDto.getPlacaVeiculo()); 
                
                idCliente = clienteDto.getIdCliente();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

