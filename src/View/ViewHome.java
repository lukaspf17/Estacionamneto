    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jTable3.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha para finalizar a locação!");
        } else {
            //Recuperando o valor do ID pela linha selecionada
            int idLocacao = (int) jTable3.getValueAt(jTable3.getSelectedRow(), 0);
            int idVaga = (int) jTable3.getValueAt(jTable3.getSelectedRow(), 1);
            
            //Confirmando se a linha selecionada é a desejada
            int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja finalizar a locação do cliente: " + jTable3.getValueAt(jTable3.getSelectedRow(), 4) + "?");
            if(confirmacao == 0){
                 //Encerra a locação, efetivamente
                LocacaoController locacaoController = new LocacaoController();
                locacaoController.offParking(idLocacao, idVaga);
                JOptionPane.showMessageDialog(null, "Locação encerrada com sucesso!");
            }
           

            //Atualizar tabela de vagas ocupadas
            readJTable();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

