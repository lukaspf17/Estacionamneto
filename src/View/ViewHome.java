    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField2.getText().isEmpty() || jFormattedTextField4.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para salvar!");
        } else {
            LocacaoController locacaoController = new LocacaoController();
            
            String text = jTextField2.getText();
            int cdVaga = Integer.parseInt(text);
            
            //Verificando se a Placa existe no banco
            boolean placaExists = locacaoController.placaValidation(jFormattedTextField4.getText());
            //Verificando se a Vaga está livre
            boolean vagaIsFree = locacaoController.vagaValidation(cdVaga);
            //Cadastrando o Veiculo na vaga
            if(placaExists && vagaIsFree){
                locacaoController.newParking(jFormattedTextField4.getText(), cdVaga);
                JOptionPane.showMessageDialog(null, "Locação feita com sucesso!");
            }
            
            //Atualizar tabela de vagas ocupadas
            readJTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

